
def cup_to_ml(cup):
    return cup * 237

def gill_to_ml(gill):
    return gill * 118

def teaspoon_to_ml(teaspoon):
    return teaspoon * 4.9

def tablespoon_to_ml(tablespoon):
    return tablespoon * 14.7868

def pint_to_ml(pint):
    return pint * 473.176

def quart_to_ml(quart):
    return quart * 946.353

def gallon_to_ml(gallon):
    return gallon * 3785.41

def ml_to_ml(ml):
    return ml

def litre_to_ml(litre):
    return litre * 1000

def ounce_to_g(ounce):
    return ounce * 28.3495

def lb_to_g(lb):
    return lb * 453.592

def g_to_g(g):
    return g
def mg_to_g(mg):
    return mg * 0.001

def dl_to_ml(dl):
    return dl * 100

